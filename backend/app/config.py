"""应用配置管理。

所有配置项通过环境变量读取，提供合理的默认值用于开发环境。
"""

from pydantic_settings import BaseSettings


class Settings(BaseSettings):
    """应用全局配置。"""

    # ------------------------------------------------------------------
    # 数据库
    # ------------------------------------------------------------------
    database_url: str = (
        "mysql+asyncmy://root:root@localhost:3306/department_tools"
    )

    # ------------------------------------------------------------------
    # JustOneAPI
    # ------------------------------------------------------------------
    justone_api_token: str = ""

    # ------------------------------------------------------------------
    # CORS
    # ------------------------------------------------------------------
    cors_origins: list[str] = [
        "http://localhost:5173",
        "http://127.0.0.1:5173",
    ]

    # ------------------------------------------------------------------
    # 应用
    # ------------------------------------------------------------------
    debug: bool = True

    model_config = {
        "env_file": ".env",
        "env_file_encoding": "utf-8",
    }


settings = Settings()
